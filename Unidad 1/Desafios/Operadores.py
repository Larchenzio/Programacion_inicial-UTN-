'''
Operadores basicos
+
-
*
%
**
'''

#Exponenciación
print(2 ** 3)
print(2 ** 3.)
print(2. ** 3)
print(2. ** 3.)

#Multiplicación
print(2 * 3)
print(2 * 3.)
print(2. * 3)
print(2. * 3.)

#División
print(6 / 3)
print(6 / 3.)
print(6. / 3)
print(6. / 3.)

#División entera
print(6 // 3)
print(6 // 3.)
print(6. // 3)
print(6. // 3.)

#Residuo (módulo)
print(14 % 4)
print(10%3)
print(10%2)

#Suma
print(-4 + 4)
print(-4. + 8)

#El operador de resta, operadores unarios y binarios
print(-4 - 4)
print(4. - 8)
print(-1.1)